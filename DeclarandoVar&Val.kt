// Declarando nome de cidades

fun variaveis() {
    var nomeCidade = "Campinas"
    println(nomeCidade)

    println("---------------")
    nomeCidade = "Valinhos"
    println(nomeCidade)
}

// A função variáveis declara uma vaiavel, que pode mudar a sua entrada

fun valor() {
    val nomeEstado = "São Paulo"
    println(nomeEstado)
}

// A função valor declara um valor, que não pode mudar sua entrada